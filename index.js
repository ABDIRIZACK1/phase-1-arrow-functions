// Function expression called divide
const divide = function() {
          return 2000 / 100;
        };
        
        // Arrow function called square
        const square = (num) => num * num;
        
        // Arrow function called add
        const add = (num1, num2) => num1 + num2;
        
        console.log(divide()); 
        console.log(square(5)); 
        console.log(add(3, 7));